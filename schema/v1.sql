CREATE TABLE urls (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    key TEXT UNIQUE NOT NULL,
    url TEXT NOT NULL,
    api_key INTEGER,
    custom INTEGER NOT NULL
);

CREATE TABLE stats (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    link_id INTEGER NOT NULL,
    time INTEGER NOT NULL
);

CREATE TABLE api (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    key TEXT UNIQUE NOT NULL,
    dlimit INTEGER NOT NULL
);

CREATE TABLE info (
    key TEXT PRIMARY KEY NOT NULL,
    value TEXT NOT NULL
);

